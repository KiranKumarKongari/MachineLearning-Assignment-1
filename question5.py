# The radius of a circle is 30 meters.
r = 30

# Calculate the area of a circle and assign the value to a variable name of _area_of_circle_
_area_of_circle_ = 3.14 * r ** 2
print("Area of circle with radius", r, "is :", _area_of_circle_)

# Calculate the circumference of a circle and assign the value to a variable name of _circum_of_circle_
_circum_of_circle_ = 2 * 3.14 * r
print("Circumference of circle with radius", r, "is :", _circum_of_circle_)

# Take radius as user input and calculate the area.
print("Enter the radius : 23")
radius = int(input())
_area_of_circle_ = 3.14 * radius ** 2
print("Area of circle with radius", radius, "is :", _area_of_circle_)
